        </h2>
        <div id="definiciones" class="accordion-collapse collapse" aria-labelledby="flush-headingOne" data-bs-parent="#accordionFlushExample">
            <div class="accordion-body">A los efectos de esta Política de Privacidad:
                <p>
                    <strong>Cuenta</strong> significa una cuenta única creada para que Usted acceda a nuestro Servicio o partes de nuestro Servicio.
                </p>
                <p>
                    <strong>Afiliado</strong> significa una entidad que controla, es controlada o está bajo control común con una parte, donde "control" significa la propiedad del 50% o más de las acciones, participación accionaria u otros valores con derecho a voto para la elección de directores u otra autoridad administrativa.
                </p>
                <p>
                    <strong>Aplicación</strong> significa el programa de software proporcionado por la Compañía descargado por Usted en cualquier dispositivo electrónico, llamado Time Control
                </p>
                <p>
                    <strong>La Compañía</strong> (referida como "la Compañía", "Nosotros", "Nos" o "Nuestro" en este Acuerdo) se refiere a Time Control.
                </p>
                <p>
                    <strong>País</strong> se refiere a: España
                </p>
                <p>
                    <strong>Dispositivo</strong> significa cualquier dispositivo que pueda acceder al Servicio, como una computadora, un teléfono celular o una tableta digit
                </p>
                <p>
                    <strong>Los datos personales</strong> son cualquier información que se relaciona con un individuo identificado o identificable.
                <p>
                    <strong>Servicio</strong> se refiere a la Aplicación.
                </p>
                <p>
                    <strong>Proveedor de servicios</strong> significa cualquier persona física o jurídica que procesa los datos en nombre de la Compañía. Se refiere a empresas de terceros o personas empleadas por la Empresa para facilitar el Servicio, proporcionar el Servicio en nombre de la Empresa, realizar servicios relacionados con el Servicio o ayudar a la Empresa a analizar cómo se utiliza el Servicio.
                </p>
                <p>
                    <strong>Los Datos de uso</strong> se refieren a los datos recopilados automáticamente, ya sea generados por el uso del Servicio o por la propia infraestructura del Servicio (por ejemplo, la duración de una visita a la página).
                </p>
                <p>
                    <strong>Usted</strong> se refiere a la persona que accede o utiliza el Servicio, o la empresa u otra entidad legal en nombre de la cual dicha persona accede o utiliza el Servicio, según corresponda.
                </p>
            </div>
        </div>
